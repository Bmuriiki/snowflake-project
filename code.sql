//MONTHLY BOOKING
CREATE VIEW gold_montly_booking AS
SELECT MONTH AS MONTH,
       COUNT(*) AS monthly_booking,
FROM hotel_main_gold
GROUP BY MONTH
ORDER BY monthly_booking DESC

//MONTHLY REVENUE USD
SELECT MONTH AS MONTH,
       COUNT(*) AS NO_OF_USD_PAYMENTS,
       SUM(TOTAL_AMOUNT) AS TOTAL_USD
FROM hotel_main_gold
WHERE CURRENCY= 'USD'
GROUP BY MONTH
ORDER BY TOTAL_USD DESC

//MONTHLY REVENUE EUR
SELECT MONTH AS MONTH,
       COUNT(*) AS NO_OF_EUR_PAYMENTS,
       SUM(TOTAL_AMOUNT) AS TOTAL_EUR
FROM hotel_main_gold
WHERE CURRENCY= 'EUR'
GROUP BY MONTH
ORDER BY TOTAL_EUR DESC

//MONTHLY REVENUE INR
