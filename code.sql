//Analysing month with most cancellation
SELECT BOOKING_STATUS,
       MONTH AS MONTH,
       YEAR AS YEAR,
       COUNT(*) AS No_of_bookings
FROM hotel_main_gold
WHERE BOOKING_STATUS='Cancelled'
GROUP BY 1,2,3
ORDER BY 4 DESC

//ROOM TYPE SUMMARY
