//Combined monthly revenue

SELECT MONTH AS MONTH,
       YEAR AS YEAR,
       CURRENCY AS CURRENCY,
       SUM(TOTAL_AMOUNT) AS TOTAL_INR
FROM hotel_main_gold
GROUP BY 1,2,3
ORDER BY 1,2


//Overview of booking status
SELECT BOOKING_STATUS,
       COUNT(*) AS No_of_bookings
FROM hotel_main_gold
